package main

import (
	"fmt"
	"strings"

	"google.golang.org/protobuf/compiler/protogen"
)

func main() {
	protogen.Options{}.Run(func(gen *protogen.Plugin) error {
		for _, f := range gen.Files {
			if !f.Generate {
				continue
			}
			generateFile(gen, f)
		}
		return nil
	})
}

func generateFile(gen *protogen.Plugin, file *protogen.File) {
	pushMessages := collectPushMessages(file)
	hasPush := len(pushMessages) > 0

	var normalServices, reverseServices []*protogen.Service
	for _, svc := range file.Services {
		if strings.HasSuffix(string(svc.Desc.Name()), "ReverseService") {
			reverseServices = append(reverseServices, svc)
		} else {
			normalServices = append(normalServices, svc)
		}
	}

	if len(normalServices) == 0 && len(reverseServices) == 0 && !hasPush {
		return
	}

	basename := file.GeneratedFilenamePrefix
	idx := strings.LastIndex(basename, "/")
	if idx != -1 {
		basename = basename[idx+1:]
	}
	filename := basename + ".flap.dart"

	g := gen.NewGeneratedFile(filename, "")

	g.P("// Code generated by protoc-gen-dart-flap. DO NOT EDIT.")
	g.P("// source: ", file.Desc.Path())
	g.P()

	if hasPush || len(reverseServices) > 0 {
		g.P("import 'dart:async';")
		g.P()
	}
	needsBridge := len(normalServices) > 0 || len(reverseServices) > 0
	if needsBridge {
		g.P("import 'package:flap/bridge/bridge.dart';")
	}
	if len(normalServices) > 0 {
		g.P("import 'package:flap/bridge/transport.dart';")
	}
	if len(reverseServices) > 0 {
		g.P("import 'package:fixnum/fixnum.dart';")
	}

	dartProtoImport := fmt.Sprintf("import '%s.pb.dart';", basename)
	g.P(dartProtoImport)

	importCount := file.Desc.Imports().Len()
	for i := 0; i < importCount; i++ {
		dep := file.Desc.Imports().Get(i)
		depPath := strings.TrimSuffix(dep.Path(), ".proto")
		idx = strings.LastIndex(depPath, "/")
		if idx != -1 {
			depPath = depPath[idx+1:]
		}
		g.P("import '", depPath, ".pb.dart';")
	}

	if hasPush || len(reverseServices) > 0 {
		g.P("import 'core.pb.dart';")
	}

	g.P()

	for _, svc := range normalServices {
		generateNormalServiceClient(g, svc)
	}

	for _, svc := range reverseServices {
		generateReverseServiceHandler(g, svc)
	}

	if hasPush {
		generatePushHandler(g, basename, pushMessages)
	}
}

// collectPushMessages returns messages whose name starts with "Push" (excluding "Push" itself).
func collectPushMessages(file *protogen.File) []*protogen.Message {
	var result []*protogen.Message
	for _, msg := range file.Messages {
		name := string(msg.Desc.Name())
		if strings.HasPrefix(name, "Push") && name != "Push" {
			result = append(result, msg)
		}
	}
	return result
}

func generatePushHandler(g *protogen.GeneratedFile, basename string, pushMessages []*protogen.Message) {
	g.P("/// PushHandler dispatches incoming fire-and-forget [Push] messages to typed streams.")
	g.P("class PushHandler {")
	g.P("  late final StreamSubscription<Push> _subscription;")
	g.P()

	for _, msg := range pushMessages {
		msgName := msg.GoIdent.GoName
		fieldSuffix := strings.TrimPrefix(msgName, "Push")
		controllerName := "_" + strings.ToLower(fieldSuffix[:1]) + fieldSuffix[1:] + "Controller"
		g.P("  final _StreamBroadcast<", msgName, "> ", controllerName, " = _StreamBroadcast();")
	}

	g.P()
	g.P("  PushHandler(Stream<Push> pushStream) {")
	g.P("    _subscription = pushStream.listen(_dispatch);")
	g.P("  }")
	g.P()

	for _, msg := range pushMessages {
		msgName := msg.GoIdent.GoName
		fieldSuffix := strings.TrimPrefix(msgName, "Push")
		streamName := strings.ToLower(fieldSuffix[:1]) + fieldSuffix[1:]
		controllerName := "_" + streamName + "Controller"
		g.P("  Stream<", msgName, "> get ", streamName, " => ", controllerName, ".stream;")
	}

	g.P()
	g.P("  void _dispatch(Push push) {")
	g.P("    switch (push.type) {")
	for _, msg := range pushMessages {
		msgName := msg.GoIdent.GoName
		fullName := string(msg.Desc.FullName())
		fieldSuffix := strings.TrimPrefix(msgName, "Push")
		streamName := strings.ToLower(fieldSuffix[:1]) + fieldSuffix[1:]
		controllerName := "_" + streamName + "Controller"
		g.P("      case '", fullName, "':")
		g.P("        ", controllerName, ".add(", msgName, ".fromBuffer(push.payload));")
		g.P("        break;")
	}
	g.P("      default:")
	g.P("        break;")
	g.P("    }")
	g.P("  }")

	g.P()
	g.P("  void dispose() {")
	g.P("    _subscription.cancel();")
	for _, msg := range pushMessages {
		msgName := msg.GoIdent.GoName
		fieldSuffix := strings.TrimPrefix(msgName, "Push")
		controllerName := "_" + strings.ToLower(fieldSuffix[:1]) + fieldSuffix[1:] + "Controller"
		_ = msgName
		g.P("    ", controllerName, ".close();")
	}
	g.P("  }")
	g.P("}")
	g.P()
	g.P("class _StreamBroadcast<T> {")
	g.P("  final _controller = StreamController<T>.broadcast();")
	g.P("  Stream<T> get stream => _controller.stream;")
	g.P("  void add(T value) => _controller.add(value);")
	g.P("  void close() => _controller.close();")
	g.P("}")
}

// generateReverseServiceHandler generates a self-contained abstract class.
// The constructor subscribes to Bridge().push and handles its own paths,
// so the implementor just needs to instantiate the subclass.
// generateReverseServiceHandler generates a self-contained abstract class.
// The constructor subscribes to Bridge().push and handles its own paths,
// so the implementor just needs to instantiate the subclass.
func generateReverseServiceHandler(g *protogen.GeneratedFile, service *protogen.Service) {
	handlerName := service.GoName + "Handler"
	noopName := "_" + service.GoName + "NoOp"
	globalVar := "_" + toCamelCase(service.GoName) + "Handler"

	// collect unary methods only
	var methods []*protogen.Method
	for _, m := range service.Methods {
		if !m.Desc.IsStreamingClient() && !m.Desc.IsStreamingServer() {
			methods = append(methods, m)
		}
	}

	g.P("/// ", handlerName, " handles Go->Dart->Go calls for ", service.GoName, ".")
	g.P("/// Extend this class and implement the abstract methods.")
	g.P("/// Instantiating the subclass is all that is needed — it self-registers,")
	g.P("/// automatically disposing the previous handler.")
	g.P("abstract class ", handlerName, " {")
	g.P("  late final Bridge _bridge;")
	g.P("  StreamSubscription<Push>? _subscription;")
	g.P()
	g.P("  ", handlerName, "() {")
	g.P("    _bridge = Bridge();")
	g.P("    ", globalVar, ".dispose();")
	g.P("    ", globalVar, " = this;")
	g.P("    _subscription = _bridge.push.listen(_dispatch);")
	g.P("  }")
	g.P()
	g.P("  // Internal constructor for no-op — skips registration and Bridge().")
	g.P("  ", handlerName, "._noOp();")
	g.P()

	for _, m := range methods {
		inName := m.Input.GoIdent.GoName
		outName := m.Output.GoIdent.GoName
		g.P("  Future<", outName, "> ", toCamelCase(m.GoName), "(", inName, " request);")
	}

	g.P()
	g.P("  void dispose() => _subscription?.cancel();")
	g.P()
	g.P("  void _dispatch(Push push) {")
	g.P("    if (push.reversePort == Int64.ZERO) return;")
	g.P("    Future<List<int>>? fut;")
	g.P("    switch (push.type) {")
	for _, m := range methods {
		fullPath := fmt.Sprintf("/%s/%s", service.Desc.FullName(), m.Desc.Name())
		inName := m.Input.GoIdent.GoName
		g.P("      case '", fullPath, "':")
		g.P("        fut = ", toCamelCase(m.GoName), "(", inName, ".fromBuffer(push.payload))")
		g.P("            .then((r) => r.writeToBuffer());")
		g.P("        break;")
	}
	g.P("    }")
	g.P("    if (fut == null) return;")
	g.P("    fut.then((payload) => _bridge.sendReverseResponse(push.reversePort, payload))")
	g.P("        .catchError((_) => _bridge.sendReverseResponse(push.reversePort, []));")
	g.P("  }")
	g.P("}")
	g.P()

	// no-op — does not call super, holds no subscription, dispose() is a no-op.
	// Stored as the initial value of the global so the real handler can call dispose() safely.
	g.P("class ", noopName, " extends ", handlerName, " {")
	g.P("  // Bypasses super constructor — no Bridge(), no subscription.")
	g.P("  ", noopName, "._() : super._noOp();")
	for _, m := range methods {
		outName := m.Output.GoIdent.GoName
		inName := m.Input.GoIdent.GoName
		g.P("  @override")
		g.P("  Future<", outName, "> ", toCamelCase(m.GoName), "(", inName, " request) async => ", outName, "();")
	}
	g.P("  @override")
	g.P("  void _dispatch(Push push) {} // no-op")
	g.P("}")
	g.P()
	g.P("// Global singleton — starts as no-op, replaced when a real handler is instantiated.")
	g.P("// ignore: prefer_final_fields")
	g.P(handlerName, " ", globalVar, " = ", noopName, "._();")
	g.P()
}

// generateNormalServiceClient generates the Dart client for a normal (Dart->Go) service.
func generateNormalServiceClient(g *protogen.GeneratedFile, service *protogen.Service) {
	serviceName := service.GoName
	clientName := serviceName + "Client"

	g.P("class ", clientName, " {")
	g.P("  final Transport _transport;")
	g.P()
	g.P("  ", clientName, "(Bridge bridge) : _transport = Transport(bridge);")
	g.P()

	for _, method := range service.Methods {
		if method.Desc.IsStreamingClient() {
			continue
		}

		reqName := method.Input.GoIdent.GoName
		resName := method.Output.GoIdent.GoName
		methodName := toCamelCase(method.GoName)
		fullPath := fmt.Sprintf("/%s/%s", service.Desc.FullName(), method.Desc.Name())

		if method.Desc.IsStreamingServer() {
			g.P("  Stream<", resName, "> ", methodName, "(", reqName, " request) async* {")
			g.P("    yield* _transport.stream(")
			g.P("      '", fullPath, "',")
			g.P("      request,")
			g.P("      () => ", resName, "(),")
			g.P("    );")
			g.P("  }")
			g.P()
		} else {
			g.P("  Future<", resName, "> ", methodName, "(", reqName, " request) async {")
			g.P("    return _transport.unary(")
			g.P("      '", fullPath, "',")
			g.P("      request,")
			g.P("      () => ", resName, "(),")
			g.P("    );")
			g.P("  }")
			g.P()
		}
	}
	g.P("}")
}

func toCamelCase(s string) string {
	if len(s) == 0 {
		return s
	}
	return strings.ToLower(s[:1]) + s[1:]
}
